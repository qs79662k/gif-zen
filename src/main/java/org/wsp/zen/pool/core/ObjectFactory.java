package org.wsp.zen.pool.core;

/**
 * 对象工厂接口，定义对象的创建、重置、验证、销毁与自身状态清理。
 * <p>
 * 实现类通过成员变量持有与对象构造相关的参数（如尺寸、容量），并将对象生命周期
 * 管理从对象池中解耦。对象池通过该接口统一的契约来获取和回收对象，而无需关心
 * 对象的具体构造细节。
 * </p>
 *
 * <h3>典型使用流程</h3>
 * <ol>
 *   <li>池请求新对象 → 调用 {@link #create()}</li>
 *   <li>对象借出使用 → 归还时调用 {@link #reset(Object)} 清理状态</li>
 *   <li>对象放回池前可选择调用 {@link #validate(Object)} 检查有效性</li>
 *   <li>池关闭或淘汰对象时调用 {@link #destroy(Object)} 释放资源</li>
 *   <li>需要重置工厂内部状态时（如文件切换）调用 {@link #clear()}</li>
 * </ol>
 *
 * @param <T> 工厂生产与管理的对象类型
 * @author wsp
 * @version 1.0
 */
public interface ObjectFactory<T> {

    /**
     * 创建一个新对象。
     * <p>
     * 对象的具体规格（如尺寸、容量）由工厂实现类的内部状态决定，因此该方法无参。
     * 如果工厂尚未具备足够的创建参数（例如自适应长度工厂还未学习到数组长度），
     * 允许返回 {@code null}。对象池调用方必须正确处理 {@code null} 返回值，
     * 典型做法是自行创建缺失的对象并立即将其归还池中，或延迟重试。
     * </p>
     *
     * @return 新创建的对象；若当前无法创建则返回 {@code null}
     */
    T create();

    /**
     * 清除工厂的内部状态，使其回到初始配置。
     * <p>
     * 此方法用于重置工厂所记忆的参数（如学习到的最大长度），通常发生在需要
     * 切换上下文、加载新文件或释放多余内存时。调用后，{@link #create()} 的
     * 行为可能改变（比如重新返回 {@code null} 等待学习）。
     * </p>
     * <p>
     * <b>重要：</b> 调用此方法的时机必须谨慎。如果对象池中仍有基于旧状态创建
     * 且尚未归还的对象，直接调用 {@code clear()} 可能导致后续行为不一致。
     * 推荐由对象池统一调度该操作，并在清空池内所有对象后执行。
     * </p>
     */
    void clear();

    /**
     * 重置对象状态，使其可安全复用。
     * <p>
     * 对象在归还给池之前调用，用于擦除上一次使用残留的数据（例如将图像填充为
     * 背景色、清空缓冲区位置指针、重置计数器等），保证下次借出时对象处于
     * “干净”状态。默认实现为空操作，子类按需覆盖。
     * </p>
     * <p>
     * 传入的对象通常不为 {@code null}，但健壮的实现应能处理 {@code null}
     * 参数（例如直接返回）。
     * </p>
     *
     * @param obj 需要重置的对象，可能为 {@code null}
     */
    default void reset(T obj) {
        // 默认无操作
    }

    /**
     * 销毁对象，释放其占用的所有资源。
     * <p>
     * 在池缩小容量、关闭或对象被判定为无效时调用。实现应在此方法中释放
     * 本地内存、关闭文件句柄、断开连接等。默认实现为空操作，子类按需覆盖。
     * </p>
     * <p>
     * 实现应安全处理 {@code null} 参数（通常直接返回）。
     * </p>
     *
     * @param obj 需要销毁的对象，可能为 {@code null}
     */
    default void destroy(T obj) {
        // 默认无操作
    }

    /**
     * 验证对象是否仍然有效、可安全复用。
     * <p>
     * 在对象被借出前调用，用于快速检测对象是否已被外部破坏或意外释放
     * （例如图片的本地资源被回收）。如果返回 {@code false}，对象池将
     * 丢弃当前对象，并调用 {@link #destroy(Object)}，然后重新创建新对象。
     * 默认实现始终返回 {@code true}，子类可按需覆盖以加入健康检查逻辑。
     * </p>
     *
     * @param obj 需要验证的对象，由池保证不为 {@code null}
     * @return {@code true} 表示对象有效，可借出使用；
     *         {@code false} 表示对象已失效，池应销毁并重建
     */
    default boolean validate(T obj) {
        return true;
    }
}